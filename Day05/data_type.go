package main

import ("fmt")

func main(){

	// Displaying integer 
	fmt.Println(34435)
	fmt.Println(0)
	fmt.Println(-34)

	// Displaying float 
	fmt.Println(-45.654)
	fmt.Println(5.0)
	fmt.Println(0.0)

	// Displaying string 
	fmt.Println(`sdfgsdf3478347^$%$%`)
	fmt.Println("asfdsdf$#%$654")

	// Displaying rune
	fmt.Println('a') // a ASCII value 97
	fmt.Println('%') // % ASCII value 37
	fmt.Println('4') // 4 ASCII value 52
	fmt.Println(')') // ) ASCII value 41

	// Displaying boolean
	fmt.Println(true)
	fmt.Println(false)
}